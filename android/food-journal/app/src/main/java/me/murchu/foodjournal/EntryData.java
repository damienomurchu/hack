package me.murchu.foodjournal;

public class EntryData {

  String timeDate;
  String hungerLevel;
  String stressLevel;
  String foodItems;
  String immediateSatisfaction;
  String actualSatisfaction;

  public void setTimeDate(String timeDate) {
    this.timeDate = timeDate;
  }

  public void setHungerLevel(String hungerLevel) {
    this.hungerLevel = hungerLevel;
  }

  public void setStressLevel(String stressLevel) {
    this.stressLevel = stressLevel;
  }

  public void setFoodItems(String foodItems) {
    this.foodItems = foodItems;
  }

  public void setImmediateSatisfaction(String immediateSatisfaction) {
    this.immediateSatisfaction = immediateSatisfaction;
  }

  public void setActualSatisfaction(String actualSatisfaction) {
    this.actualSatisfaction = actualSatisfaction;
  }
}
